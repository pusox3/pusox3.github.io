var container = document.getElementById('mynetwork');

var nodes = new vis.DataSet(nodeSet);
var edges = new vis.DataSet(edgeSet);
// provide the data in the vis format
var data = {
    nodes: nodes,
    edges: edges
};

var options = {
    autoResize: true,
    height: '100%',
    width: '100%',
    locale: 'en',
    nodes: {
        borderWidth: 2,
        borderWidthSelected: 3,
        color: {
            border: '#fdcb6e',
            background: '#ffeaa7',
            highlight: {
                border: '#fdcb6e',
                background: '#fdcb6e'
            }
        }
    },
    edges: {
        width: 3,
        arrows: {
            to: { enabled: true, type: 'arrow' }
        },
        scaling: {
            label: true,
        },
        shadow: true,
        smooth: {
            enabled: true,
            type: 'dynamic',
            roundness: 0.5
        }
    },
    layout: {
        improvedLayout: false,
    },
    physics: {
        enabled: true,
        solver: 'forceAtlas2Based'
    }
}

var network = new vis.Network(container, data, options);

network.on("stabilizationProgress", function (params) {
    var maxWidth = container.clientWidth;
    var minWidth = 20;
    var widthFactor = params.iterations / params.total;
    var width = Math.max(minWidth, maxWidth * widthFactor);
    document.getElementById('bar').style.width = width + 'px';
    document.getElementById('text').innerHTML = Math.round(widthFactor * 100) + '%';
});

network.once("stabilizationIterationsDone", function () {
    document.getElementById('text').innerHTML = '100%';
    document.getElementById('loadingBar').style.opacity = 0;
    document.getElementById('bar').style.width = container.clientWidth;
    setTimeout(function () {
        document.getElementById('loadingBar').style.display = 'none';
    }, 500);
});

function find_individual(name) {
    nodes.getDataSet().get().some(function (node) {
        if (name.toLowerCase() == node.label.toLowerCase()) {
            network.focus(node.id, { scale: 2 });
            return true;
        }
    })
}

$('#web-search').click(function () {
    var name = $('#web-text-input').val();
    find_individual(name);
});